function normalizeText(value: string): string {
  return value.replace(/\s+/g, " ").trim();
}

function preserveOuterWhitespace(original: string, translated: string): string {
  const leading = original.match(/^\s*/)?.[0] ?? "";
  const trailing = original.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

function looksTechnical(value: string): boolean {
  const text = value.trim();
  if (!text) return true;
  if (/^(\/|\.\/|~\/|[A-Za-z]:\\)/.test(text)) return true;
  if (/^[A-Z0-9_*-]+$/.test(text) && text.length > 2) return true;
  if (/^[a-z]+_[a-z0-9_]+$/.test(text)) return true;
  if (/^[\w@./:-]+\.(ts|tsx|js|jsx|json|md|sh|yml|yaml)$/.test(text)) return true;
  if (/^(pnpm|npm|npx|node|git|curl|cd|pwd|POST|GET|PUT|PATCH|DELETE)\b/.test(text)) return true;
  if (/^\[[0-9:.]+\]\s/.test(text)) return true;
  if (/^https?:\/\//.test(text)) return true;
  return false;
}

function shouldSkipElement(element: Element | null): boolean {
  if (!element) return false;
  if (SKIP_TAGS.has(element.tagName)) return true;
  return Boolean(element.closest(SKIP_SELECTOR));
}

function shouldSkipElementAttributes(element: Element): boolean {
  return Boolean(element.closest([
    "code",
    "kbd",
    "pre",
    "script",
    "style",
    "samp",
    "[contenteditable='true']",
    "[data-chinese-ui-skip]",
    "[data-language]",
    ".cm-editor",
    ".cm-content",
    ".mdxeditor",
  ].join(",")));
}

export function translateUiText(value: string): string {
  const normalized = normalizeText(value);
  if (!normalized || looksTechnical(normalized)) return value;

  const exact = EXACT_TRANSLATIONS[normalized];
  if (exact) return preserveOuterWhitespace(value, exact);

  for (const [pattern, replacer] of PATTERN_TRANSLATIONS) {
    const match = normalized.match(pattern);
    if (match) return preserveOuterWhitespace(value, replacer(match));
  }

  return value;
}

export function translateUiTitle(value: string): string {
  return value
    .split(" • ")
    .map((part) => translateUiText(part))
    .join(" • ");
}

function translateDocumentTitle() {
  if (typeof document === "undefined" || !document.title) return;
  const next = translateUiTitle(document.title);
  if (next !== document.title) document.title = next;
}

function translateTextNode(node: Text) {
  const parent = node.parentElement;
  if (shouldSkipElement(parent)) return;
  const next = translateUiText(node.nodeValue ?? "");
  if (next !== node.nodeValue) {
    node.nodeValue = next;
  }
}

function translateAttributes(element: Element) {
  if (shouldSkipElementAttributes(element)) return;
  for (const attributeName of ATTRIBUTE_NAMES) {
    const value = element.getAttribute(attributeName);
    if (!value) continue;
    const next = translateUiText(value);
    if (next !== value) element.setAttribute(attributeName, next);
  }
}

function translateTree(root: Node) {
  if (root.nodeType === Node.TEXT_NODE) {
    translateTextNode(root as Text);
    return;
  }

  if (!(root instanceof Element || root instanceof DocumentFragment || root instanceof Document)) return;
  if (root instanceof Element) {
    translateAttributes(root);
    if (shouldSkipElement(root)) return;
  }

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
  let current = walker.nextNode();
  while (current) {
    if (current.nodeType === Node.ELEMENT_NODE) {
      const element = current as Element;
      translateAttributes(element);
      if (shouldSkipElement(element)) {
        current = walker.nextSibling();
        continue;
      }
    } else if (current.nodeType === Node.TEXT_NODE) {
      translateTextNode(current as Text);
    }
    current = walker.nextNode();
  }
}

export function installChineseUiRuntime(root: ParentNode = document.body): { disconnect: () => void } {
  translateTree(root as Node);
  translateDocumentTitle();

  const observer = new MutationObserver((mutations) => {
    for (const mutation of mutations) {
      if (mutation.type === "characterData") {
        translateTree(mutation.target);
        continue;
      }
      if (mutation.type === "attributes") {
        translateTree(mutation.target);
        continue;
      }
      for (const node of mutation.addedNodes) {
        translateTree(node);
      }
    }
  });

  observer.observe(root, {
    attributes: true,
    attributeFilter: [...ATTRIBUTE_NAMES],
    characterData: true,
    childList: true,
    subtree: true,
  });

  const titleObserver = new MutationObserver(() => translateDocumentTitle());
  const titleElement = document.querySelector("title");
  if (titleElement) {
    titleObserver.observe(titleElement, { childList: true, characterData: true, subtree: true });
  }

  return {
    disconnect: () => {
      observer.disconnect();
      titleObserver.disconnect();
    },
  };
}
